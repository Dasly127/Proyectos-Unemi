


"""
Calcular el área y perimetro de un rectangulo
"""

altura = int(input("Proporciona la altura del rectangulo => "))
base = int(input("Proporciona la base del rectangulo => "))
area_rectangulo = base * altura
perimetro = (base + altura) * 2

print(f"el aréa del rectangulo es: {area_rectangulo}")
print(f"el perímetro del rectangulo es: {perimetro}")